			<h1 class="text-center">Règlement intérieur</h1>

			<h2>Le calme et la tranquilité avant tout :</h2>

			<p>– Laissons l’agitation à l’extérieur, pour expérimenter la paix intérieure : le silence est ressourçant, il permet la relaxation, la concentration, l’observation de soi.</p>

			<p>Le silence est demandé dès l’entrée dans la salle et jusqu’à la sortie (chuchotement dans les vestiaires) afin de respecter la quiétude des personnes et du lieu.</p>

			<p>– Les professeurs ont besoin de moment de calme pour se centrer, avant le cours et vous aider ainsi à trouver la paix en vous. Ils sont à l’écoute de vos questions après le cours. Ils peuvent demander d’interrompre la discussion si besoin pour se préparer au cours suivant.</p>


			<h2>Votre santé :</h2>

			<p>– Un certificat médical doit être remis en début d’année, comportant la mention « aucune contre-indication à la pratique du yoga »</p>

			<p>– Vous êtes tenu responsable d’informer chaque professeur au début de chaque cours de votre état de santé (grossesse, problème cardiaque, de dos, d’articulations…)</p>


			<h2>Les bons conseils :</h2>

			<p>– S’assurer d’avoir l’estomac presque vide avant la pratique. Un passage aux toilettes peut aussi éviter d’être indisposé et de s’interrompre en pleine séance.</p>

			<p>– Penser à s’hydrater (boire de l’eau ou de la tisane) après la pratique.</p>

			<p>– Etre bien dans ses vêtements : ceux-ci seront adaptés à la pratique du yoga, confortables, souples, extensibles et modestes.</p>

			<p>– Eviter de porter des bijoux ou objets métalliques pouvant gêner ou blesser, poser ses lunettes dans leur boitier. Retirer sa montre, son rythme contrariant le rythme naturel recherché pendant les pratiques.</p>

			<p>– Eviter de mettre du parfum avant la séance, cela ne sert à rien, peut importuner les autres pratiquants et l’effet est souvent inverse à celui recherché.</p>


			<h2>La pratique :</h2>

			<p>– Arrivée 5 à 10 minutes avant le début des séances afin de se changer, s’installer et se préparer intérieurement à sa pratique. Arriver un peu en avance permet de se poser calmement sur votre tapis.</p>

			<p>– En cas de retard, être discret et attendre la fin de la relaxation initiale ou du rituel avant d’entrer discrètement dans la salle.</p>

			<p>S’Assurer d’avoir pris le matériel adapté à votre pratique personnelle (coussin, couverture, sangles, briques…) à disposition dans la salle.</p>

			<p>– La pratique se fait pieds nus. Pour des raisons d’hygiène, merci de passer du spray désinfectant avec un chiffon (à disposition dans la salle) sur le tapis que vous aviez emprunté.</p>

			<p>– Respect du matériel (lors du rangement des tapis notamment), des lieux et des autres pratiquants.</p>

			<p>– Les effets personnels (sac à main et autres sacs, téléphone…) restent impérativement au vestiaire (que nous pouvons fermer à clé, à votre demande), ZENTOPIA n’est pas responsable en cas de perte ou de vol.</p>


			<h2>Les règles de bonne conduite :</h2>

			<p>– Une séance de yoga est un moment privilégié avec soi-même mais aussi avec les autres, merci d’éteindre les portables et de les laisser dans le vestiaire ainsi que tout autre appareil d’enregistrement ou de photo.</p>

			<p>-Penser à enlever ses chaussures à l’entrée (pour se sentir comme à la maison les chaussons d’hiver sont bien venus).</p>

			<p>– Toutes consommations d’alcool ou de substances illicites sont formellement interdites pour le bon déroulement de votre pratique et celle des autres. Le yoga est déjà une expérience forte en elle-même.</p>

			<p>– Le tutoiement est vivement conseillé. Un ton courtois et une attitude positive aident  à maintenir la paix et la pureté dans notre espace.</p>

			<p>– Hélas nos compagnons à quatre pattes ne peuvent être admis dans les locaux.</p>

			<br/>
			<p>Nous sommes à votre écoute, pour toute demande, vous pouvez nous écrire à contact@zentopia.fr ou via le formulaire de contact du site.</p>

